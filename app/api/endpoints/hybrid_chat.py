class HybridChatRequest(BaseModel):
    session_id: Optional[UUID] = None
    message: str = Field(..., min_length=1, max_length=10000)
    user_id: str
    repo_id: Optional[str] = None
    role: Optional[str] = None
    company_id: Optional[str] = None
    channel: str = "web"
    debug: bool = False  # Include pipeline_breakdown in response
    metadata: dict = Field(default_factory=dict)
    # Pre-fetched by MARS from SSO — skips COSMOS internal SSO login.
    # MARS simulation flow: MARS fetches seller token → passes here →
    # COSMOS uses it directly for live Shiprocket API calls (act mode).
    seller_token: Optional[str] = None
    sso_token: Optional[str] = None   # alias, same token different name
    # ICRM admin token — generated by MARS on MCP session create from user_sso_tokens DB.
    # Used for /v1/admin/* and /api/v1/admin/* endpoints (requires company_id=1 role).
    icrm_token: Optional[str] = None
    tournament_mode: bool = False     # A/B strategy testing via TournamentEngine
    # Write action approval gate (M3-P1 #22)
    # confirm_action=True + confirm_token=<token> re-executes a pending write action.
    confirm_action: bool = False
    confirm_token: Optional[str] = None


class PipelineBreakdown(BaseModel):
    pipeline: str
    contributed: bool
    latency_ms: float
    items: int
    stages: list


class HybridChatResponse(BaseModel):
    session_id: UUID
    message_id: UUID
    content: str
    intents: list = Field(default_factory=list)
    confidence: float = 0.0
    needs_clarification: bool = False
    clarification_prompt: Optional[str] = None
    tools_used: list = Field(default_factory=list)
    # Attribution (only when debug=True)
    pipeline_breakdown: Optional[Dict[str, Any]] = None
    timing: Optional[Dict[str, float]] = None
    signal: Optional[Dict[str, Any]] = None
    wave_trace: Optional[Dict[str, Any]] = None


