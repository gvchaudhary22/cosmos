@router.post("/chat/stream")
async def hybrid_chat_stream(request: Request, chat_req: HybridChatRequest):
    """
    SSE streaming hybrid chat. Emits per-pipeline events so the frontend
    can show real-time progress through each stage.

    Events:
    - stage:probe_start     — Stage 1 starting
    - probe:{pipeline}      — Individual probe result
    - stage:probe_complete  — All probes done
    - stage:deep_start      — Stage 2 starting
    - deep:{pipeline}       — Individual deep result (or skipped)
    - stage:deep_complete   — Deep stage done
    - stage:llm_start       — LLM assembly starting
    - chunk                 — LLM response text chunks
    - attribution           — Full pipeline breakdown
    - done                  — Final metadata
    - error                 — Error information
    """
    orchestrator = _get_orchestrator(request)
    session_id = str(chat_req.session_id or uuid4())

    async def generate():
        try:
            if orchestrator is None:
                yield _sse("error", {"message": "Hybrid orchestrator not initialized"})
                return

            # --- Approval confirmation path (M3-P1 #22) ---
            # When the client re-sends with confirm_action=True + confirm_token,
            # skip the full pipeline and directly execute the pre-approved action.
            if chat_req.confirm_action and chat_req.confirm_token:
                gate = _get_approval_gate(request)
                if gate is None:
                    yield _sse("error", {"message": "Action approval gate not initialized"})
                    yield _sse("done", {"session_id": session_id, "confidence": 0.0, "intents": []})
                    return
                proposal = await gate.consume(chat_req.confirm_token)
                if proposal is None:
                    yield _sse("error", {
                        "message": "Confirmation token is invalid or has expired. Please re-submit the original request.",
                    })
                    yield _sse("done", {"session_id": session_id, "confidence": 0.0, "intents": []})
                    return
                # SECURITY: validate session ownership — prevent cross-session token reuse
                if proposal.session_id != str(session_id):
                    logger.warning(
                        "action_approval.session_mismatch",
                        proposal_session=proposal.session_id,
                        request_session=str(session_id),
                    )
                    yield _sse("error", {"message": "Confirmation token does not belong to this session."})
                    yield _sse("done", {"session_id": session_id, "confidence": 0.0, "intents": []})
                    return
                yield _sse("action_executing", {
                    "action": proposal.action_type,
                    "summary": proposal.summary,
                    "risk_level": proposal.risk_level,
                })
                try:
                    from app.config import settings as _settings
                    from app.services.tool_executor import ToolExecutorService, SessionContext as _SC
                    _executor = ToolExecutorService(settings=_settings, db=None)
                    _ctx = _SC(
                        seller_token=chat_req.seller_token,
                        icrm_token=chat_req.icrm_token,
                        approved=True,
                        approved_job_id=proposal.action_type,
                    )
                    exec_result = await _executor.execute(
                        proposal.action_type, proposal.action_input, _ctx
                    )
                    await _executor.aclose()
                except Exception as _exec_exc:
                    logger.error("action_approval.execute_error", error=str(_exec_exc))
                    yield _sse("error", {"message": f"Action execution failed: {str(_exec_exc)}"})
                    yield _sse("done", {"session_id": session_id, "confidence": 0.0, "intents": []})
                    return
                yield _sse("action_executed", {
                    "action": exec_result.tool_name,
                    "status": exec_result.status,
                    "data": exec_result.data,
                    "error": exec_result.error,
                    "latency_ms": round(exec_result.latency_ms, 1),
                })
                _ok = exec_result.status == "success"
                _summary_text = (
                    f"{proposal.summary} — executed successfully."
                    if _ok else
                    f"Action could not be completed: {exec_result.error or exec_result.status}"
                )
                yield _sse("chunk", {"text": _summary_text})
                yield _sse("done", {
                    "session_id": session_id,
                    "confidence": 1.0 if _ok else 0.0,
                    "tools_used": [exec_result.tool_name],
                    "intents": [],
                })
                return

            # Phase 5f: Wave progress queue — bridge between orchestrator callback
            # and SSE generator. The callback enqueues events; we drain them at
            # natural yield points so the client sees per-wave progress in real time.
            import asyncio as _asyncio
            _wave_queue: _asyncio.Queue = _asyncio.Queue()

            async def _on_wave_progress(wave_id, task_id, status, data):
                await _wave_queue.put({
                    "wave_id": wave_id,
                    "task_id": task_id,
                    "status": status,
                    **data,
                })

            async def _drain_wave_queue():
                """Yield all pending wave progress events from the queue."""
                while not _wave_queue.empty():
                    event = _wave_queue.get_nowait()
                    yield _sse(f"wave:{event['wave_id']}:{event['status']}", event)

            # --- Stage 1: Parallel Probe ---
            yield _sse("stage:probe_start", {"pipelines": [
                "intent_classifier", "entity_lookup", "vector_search",
                "page_role", "cross_repo",
            ]})

            probe_start = time.monotonic()
            # Phase 5f: inject wave progress callback so WaveExecutor emits events
            orchestrator._wave_progress_cb = _on_wave_progress
            probe_results = await orchestrator._stage1_parallel_probe(
                query=chat_req.message,
                user_id=chat_req.user_id,
                repo_id=chat_req.repo_id,
                role=chat_req.role,
                session_context=chat_req.metadata,
            )
            probe_ms = (time.monotonic() - probe_start) * 1000

            # Drain wave progress events from W1
            async for wave_event in _drain_wave_queue():
                yield wave_event

            # Emit individual probe results
            for pipeline, pr in probe_results.items():
                yield _sse(f"probe:{pipeline.value}", {
                    "found_data": pr.found_data,
                    "latency_ms": round(pr.latency_ms, 1),
                    "recommend_deepen": pr.recommend_deepen,
                    "reason": pr.reason,
                    "error": pr.error,
                })

            yield _sse("stage:probe_complete", {
                "latency_ms": round(probe_ms, 1),
                "pipelines_with_data": [
                    p.value for p, r in probe_results.items() if r.found_data
                ],
            })

            # --- Stage 2: Conditional Deepening ---
            deep_decisions = orchestrator._route_deep(probe_results, chat_req.message)

            fired = [p.value for p, d in deep_decisions.items() if d["fire"]]
            skipped = [p.value for p, d in deep_decisions.items() if not d["fire"]]

            yield _sse("stage:deep_start", {
                "firing": fired,
                "skipping": skipped,
                "decisions": {p.value: d for p, d in deep_decisions.items()},
            })

            deep_start = time.monotonic()
            deep_results = await orchestrator._stage2_conditional_deep(
                deep_decisions, probe_results, chat_req.message, chat_req.repo_id,
            )
            deep_ms = (time.monotonic() - deep_start) * 1000

            # Drain wave progress events from W2
            async for wave_event in _drain_wave_queue():
                yield wave_event

            for pipeline, dr in deep_results.items():
                yield _sse(f"deep:{pipeline.value}", {
                    "found_data": dr.found_data,
                    "latency_ms": round(dr.latency_ms, 1),
                    "error": dr.error,
                })

            yield _sse("stage:deep_complete", {"latency_ms": round(deep_ms, 1)})

            # --- Build full orchestrator result for attribution ---
            orch_result = OrchestratorResult()
            orch_result.probe_latency_ms = probe_ms
            orch_result.deep_latency_ms = deep_ms
            # Bug B2 fix: _merge_context is async — must be awaited
            orch_result.context = await orchestrator._merge_context(probe_results, deep_results)

            # Extract intents
            from app.services.query_orchestrator import PipelineName
            intent_probe = probe_results.get(PipelineName.INTENT)
            if intent_probe and intent_probe.data:
                orch_result.intents = intent_probe.data if isinstance(intent_probe.data, list) else [intent_probe.data]

            # Bug B3 fix: run ParamClarificationEngine on streaming path
            # (previously only ran in orchestrator.execute() — non-streaming path)
            try:
                _kb_root = getattr(orchestrator, "_kb_root_for_clarifier", None)
                if _kb_root:
                    _clarifier = getattr(orchestrator, "_param_clarifier", None)
                    if _clarifier is None:
                        from app.brain.param_clarifier import ParamClarificationEngine
                        _clarifier = ParamClarificationEngine(_kb_root)
                        orchestrator._param_clarifier = _clarifier
                    _kc = (
                        orch_result.context.get("knowledge_chunks", [])
                        if isinstance(orch_result.context, dict) else []
                    )
                    _clarification = await _clarifier.check(
                        knowledge_chunks=_kc,
                        query=chat_req.message,
                        company_id=chat_req.company_id or None,
                        session_context=chat_req.metadata or {},
                    )
                    if _clarification:
                        yield _sse("clarification", {
                            "question": _clarification.question,
                            "pending_param": _clarification.pending_param,
                            "api_entity_id": _clarification.api_entity_id,
                        })
                        yield _sse("done", {
                            "session_id": session_id,
                            "needs_clarification": True,
                            "confidence": 0.0,
                            "intents": orch_result.intents,
                        })
                        return
            except Exception as _ce:
                logger.debug("stream.param_clarifier_error", error=str(_ce))

            # --- Write action approval gate (M3-P1 #22) ---
            # Detect cancel-order (and future write action) intents BEFORE LLM streaming.
            # If detected → propose approval, yield approval_required SSE event, return.
            # Operator then re-sends with confirm_action=True + confirm_token to execute.
            try:
                _gate = _get_approval_gate(request)
                if _gate is not None and not chat_req.confirm_action:
                    from app.brain.action_approval import ActionApprovalGate
                    _kc_for_gate = (
                        orch_result.context.get("knowledge_chunks", [])
                        if isinstance(orch_result.context, dict) else []
                    )
                    _write_signal = ActionApprovalGate.detect_write_action(
                        chat_req.message, orch_result.intents, _kc_for_gate
                    )
                    if _write_signal is not None:
                        _proposal = await _gate.propose(
                            session_id=str(session_id),
                            action_type=_write_signal.tool_name,
                            action_input=_write_signal.action_input,
                            summary=_write_signal.summary,
                            risk_level=_write_signal.risk_level,
                        )
                        logger.info(
                            "stream.approval_required",
                            action=_write_signal.tool_name,
                            entity_id=_write_signal.entity_id,
                            token_prefix=_proposal.confirm_token[:8],
                        )
                        yield _sse("approval_required", {
                            "confirm_token": _proposal.confirm_token,
                            "expires_in_seconds": _proposal.ttl_seconds(),
                            "action": _proposal.action_type,
                            "action_input": _proposal.action_input,
                            "summary": _proposal.summary,
                            "risk_level": _proposal.risk_level,
                            "message": (
                                f"This action requires your confirmation: {_proposal.summary}. "
                                f"Re-send with confirm_action=true and the confirm_token provided in this event."
                            ),
                        })
                        yield _sse("done", {
                            "session_id": session_id,
                            "needs_clarification": False,
                            "pending_approval": True,
                            "confidence": 0.9,
                            "intents": orch_result.intents,
                        })
                        return
            except Exception as _ae:
                logger.debug("stream.action_approval_error", error=str(_ae))

            # --- Analytics probe (M3-P2 #24) ---
            # If the analytics probe fired with a company_id, execute the live count
            # and yield an `analytics` SSE event before the LLM response.
            try:
                from app.services.query_orchestrator import PipelineName as _PN
                _analytics_probe = orch_result.pipeline_contributions.get(_PN.ANALYTICS) if hasattr(orch_result, "pipeline_contributions") else None
                # Check probe_results directly on orch_result if available
                _analytics_data = None
                if hasattr(orch_result, "probe_results"):
                    _ap = orch_result.probe_results.get(_PN.ANALYTICS)
                    if _ap and _ap.found_data and _ap.data:
                        _analytics_data = _ap.data
                if _analytics_data and chat_req.icrm_token:
                    from app.config import settings as _settings
                    from app.services.tool_executor import ToolExecutorService, SessionContext as _SC
                    _tool_name = _analytics_data.get("tool_name", "analytics_order_count")
                    _tool_params = {"client_id": int(_analytics_data["company_id"])}
                    if _analytics_data.get("from_date"):
                        _tool_params["from"] = _analytics_data["from_date"]
                    if _analytics_data.get("to_date"):
                        _tool_params["to"] = _analytics_data["to_date"]
                    _executor = ToolExecutorService(settings=_settings, db=None)
                    _actx = _SC(
                        seller_token=chat_req.seller_token,
                        icrm_token=chat_req.icrm_token,
                        approved=True,
                    )
                    try:
                        _result = await _executor.execute(_tool_name, _tool_params, _actx)
                        await _executor.aclose()
                        if _result.status == "success":
                            _count = (
                                _result.data.get("total")
                                or _result.data.get("count")
                                or len(_result.data) if isinstance(_result.data, list) else None
                            )
                            yield _sse("analytics", {
                                "metric": _tool_name,
                                "company_id": _analytics_data["company_id"],
                                "value": _count,
                                "label": _analytics_data.get("date_label"),
                                "from_date": _analytics_data.get("from_date"),
                                "to_date": _analytics_data.get("to_date"),
                                "raw": _result.data,
                            })
                    except Exception as _ae:
                        logger.debug("analytics_probe.execute_error", error=str(_ae))
                        # Graceful degradation — fall through to LLM text answer
            except Exception as _ae:
                logger.debug("analytics_probe.error", error=str(_ae))

            # --- Stage 3: LLM Assembly ---
            yield _sse("stage:llm_start", {})

            llm_context = _build_llm_context(orch_result)
            engine = _get_react_engine(request)

            if engine is not None:
                augmented_context = {
                    "user_id": chat_req.user_id,
                    "company_id": chat_req.company_id,
                    "channel": chat_req.channel,
                    "pipeline_context": llm_context,
                }
                augmented_context.update(chat_req.metadata)

                # Bug B1 fix: classification was undefined in this scope.
                # Use request_classification if populated, else empty dict.
                # complexity defaults to "standard" which is correct for most queries.
                classification = orch_result.request_classification or {}

                # True token-level streaming via RIPER.stream_final_response()
                t0 = time.monotonic()
                riper = getattr(orchestrator, "riper_engine", None)

                if riper is not None and hasattr(riper, "stream_final_response"):
                    # True streaming: yields {"event":"phase"|"chunk"|"done", ...}
                    async for item in riper.stream_final_response(
                        query=chat_req.message,
                        context=orch_result.context,
                        intents=orch_result.intents,
                        complexity=classification.get("complexity", "standard"),
                    ):
                        ev = item.get("event", "chunk")
                        if ev == "chunk":
                            yield _sse("chunk", {"text": item.get("text", "")})
                        elif ev == "phase":
                            yield _sse(f"riper_phase:{item.get('phase', '')}", item)
                        elif ev == "done":
                            confidence = item.get("confidence", 0.5)
                            tools_used = item.get("tools_used", [])
                else:
                    # Fallback: non-streaming ReAct process + chunked emit
                    result = await engine.process(chat_req.message, augmented_context)
                    response_text = result.response
                    for i in range(0, len(response_text), 40):
                        yield _sse("chunk", {"text": response_text[i:i + 40]})
                    confidence = result.confidence
                    tools_used = result.tools_used

                llm_ms = (time.monotonic() - t0) * 1000
            else:
                content = f"Based on pipeline analysis:\n\n{llm_context}"
                for i in range(0, len(content), 40):
                    yield _sse("chunk", {"text": content[i:i + 40]})
                confidence = 0.5
                tools_used = []
                llm_ms = 0.0

            # Attribution
            # Build attribution manually for streaming
            attribution = {
                "pipeline_breakdown": {},
                "timing": {
                    "probe_ms": round(probe_ms, 1),
                    "deep_ms": round(deep_ms, 1),
                    "llm_ms": round(llm_ms, 1),
                    "total_ms": round(probe_ms + deep_ms + llm_ms, 1),
                },
            }
            for p, pr in probe_results.items():
                attribution["pipeline_breakdown"][p.value] = {
                    "stage": "probe",
                    "contributed": pr.found_data,
                    "latency_ms": round(pr.latency_ms, 1),
                }
            for p, dr in deep_results.items():
                attribution["pipeline_breakdown"][p.value] = {
                    "stage": "deep",
                    "contributed": dr.found_data,
                    "latency_ms": round(dr.latency_ms, 1),
                }
            for p, d in deep_decisions.items():
                if not d["fire"] and p.value not in attribution["pipeline_breakdown"]:
                    attribution["pipeline_breakdown"][p.value] = {
                        "stage": "deep",
                        "contributed": False,
                        "skipped": True,
                        "reason": d["reason"],
                    }

            yield _sse("attribution", attribution)

            yield _sse("done", {
                "session_id": session_id,
                "confidence": confidence,
                "tools_used": tools_used,
                "intents": orch_result.intents,
            })

        except Exception as exc:
            logger.error("hybrid_chat_stream.error", error_code="ERR-COSMOS-500", error=type(exc).__name__)
            yield _sse("error", {"message": "An internal error occurred. Please try again."})

    return StreamingResponse(
        generate(),
        media_type="text/event-stream",
        headers={
            "Cache-Control": "no-cache",
            "Connection": "keep-alive",
            "X-Accel-Buffering": "no",
        },
    )


